package com.tyss.cg.operators;

public class RelationOperator {
	public static void main(String[] args) {
		int a = 97;
		int b = 96;

		System.out.println("i less than j= " + (a < b));
		System.out.println("i greater than j= " + (a > b));
		System.out.println("i less than or equal to j= " + (a <= b));
		System.out.println("i greater than or equal to j= " + (a >= b));
		System.out.println("i not equal to j= " + (a != b));
		System.out.println("i equal to j= " + (a == b));

	}

}
