package com.tyss.cg.datatypesandvariables;

public class PrimitiveDatatypes {
public static void main(String[] args) {
	byte b = 12; //declaration and initialization
	short s= 24; //declaration and initialization
	int i= 36;  //declaration and initialization
	long l=4857576576575665757L; //declaration and initialization
	float f=12.890F; //declaration and initialization
	double d=12.890D; //declaration and initialization
	char c='A'; //declaration and initialization
	boolean b1=true; //declaration and initialization
	
	System.out.println(b); //utilization
	System.out.println(s); //utilization
	System.out.println(i);//utilization
	System.out.println(l);//utilization
	System.out.println(f);//utilization
	System.out.println(d);//utilization
	System.out.println(c);//utilization
	System.out.println(b1);//utilization
	
	
}
}
